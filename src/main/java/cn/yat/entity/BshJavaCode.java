package cn.yat.entity;

public class BshJavaCode {
    private Integer opsId;

    private String bshArgs;

    private String code;

    public Integer getOpsId() {
        return opsId;
    }

    public void setOpsId(Integer opsId) {
        this.opsId = opsId;
    }

    public String getBshArgs() {
        return bshArgs;
    }

    public void setBshArgs(String bshArgs) {
        this.bshArgs = bshArgs;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}